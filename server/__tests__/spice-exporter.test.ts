describe('exportSpiceNetlist — edge cases', () => {
  it('handles empty instances list', () => {
    const output = exportSpiceNetlist(makeInput({ instances: [] }));
    expect(output.netlist).toContain('.END');
  });

  it('skips instances with missing parts and adds warning', () => {
    const output = exportSpiceNetlist(makeInput({
      instances: [makeInstance({ id: 1, partId: 999, referenceDesignator: 'U1' })],
      parts: [], // no parts
    }));

    expect(output.netlist).toContain('part not found');
    expect(output.warnings.some(w => w.includes('not found'))).toBe(true);
  });

  it('skips components with no connectors and adds warning', () => {
    const output = exportSpiceNetlist(makeInput({
      instances: [makeInstance({ id: 1, partId: 100, referenceDesignator: 'U1' })],
      parts: [makePart({ id: 100, meta: { family: 'resistor' }, connectors: [] })],
    }));

    expect(output.warnings.some(w => w.includes('no connectors'))).toBe(true);
  });
});
