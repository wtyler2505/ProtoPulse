import { describe, it, expect } from 'vitest';
import {
  exportSpiceNetlist,
  type SpiceExportInput,
  type SpiceExportConfig,
} from '../export/spice-exporter';
import type { CircuitInstanceRow, CircuitNetRow, ComponentPart } from '@shared/schema';

// =============================================================================
// Fixtures — typed to match Drizzle-inferred row shapes
// =============================================================================

function makeInstance(overrides: Partial<CircuitInstanceRow> = {}): CircuitInstanceRow {
  return {
    id: 1,
    circuitId: 1,
    partId: 100,
    subDesignId: null,
    referenceDesignator: 'R1',
    schematicX: 0,
    schematicY: 0,
    schematicRotation: 0,
    breadboardX: null,
    breadboardY: null,
    breadboardRotation: 0,
    pcbX: null,
    pcbY: null,
    pcbRotation: 0,
    pcbSide: 'front',
    properties: {},
    createdAt: new Date(),
    ...overrides,
  };
}

function makeNet(overrides: Partial<CircuitNetRow> = {}): CircuitNetRow {
  return {
    id: 1,
    circuitId: 1,
    name: 'NET1',
    netType: 'signal',
    voltage: null,
    busWidth: null,
    segments: [],
    labels: [],
    style: {},
    createdAt: new Date(),
    ...overrides,
  };
}

function makePart(overrides: Partial<ComponentPart> = {}): ComponentPart {
  return {
    id: 100,
    projectId: 1,
    nodeId: null,
    meta: { family: 'resistor', title: 'Resistor' },
    connectors: [
      { id: 'pin1', name: 'PIN1', connectorType: 'pad', shapeIds: {} },
      { id: 'pin2', name: 'PIN2', connectorType: 'pad', shapeIds: {} },
    ],
    buses: [],
    views: {},
    constraints: [],
    version: 1,
    createdAt: new Date(),
    updatedAt: new Date(),
    ...overrides,
  };
}

function makeConfig(overrides: Partial<SpiceExportConfig> = {}): SpiceExportConfig {
  return {
    analysis: 'op',
    ...overrides,
  };
}

function makeInput(overrides: Partial<SpiceExportInput> = {}): SpiceExportInput {
  return {
    circuitName: 'Test Circuit',
    instances: [],
    nets: [],
    parts: [],
    config: makeConfig(),
    ...overrides,
  };
}

// =============================================================================
// Basic output structure
// =============================================================================

describe('exportSpiceNetlist — basic output', () => {
  it('returns netlist, filename, and warnings', () => {
    const output = exportSpiceNetlist(makeInput());

    expect(output).toHaveProperty('netlist');
    expect(output).toHaveProperty('filename');
    expect(output).toHaveProperty('warnings');
    expect(typeof output.netlist).toBe('string');
    expect(typeof output.filename).toBe('string');
    expect(Array.isArray(output.warnings)).toBe(true);
  });

  it('generates a sanitized filename from circuit name', () => {
    const output = exportSpiceNetlist(makeInput({ circuitName: 'My Circuit / Rev 2' }));
    expect(output.filename).toBe('my_circuit___rev_2.cir');
  });

  it('uses default filename for empty circuit name', () => {
    const output = exportSpiceNetlist(makeInput({ circuitName: '' }));
    expect(output.filename).toBe('circuit.cir');
  });

  it('includes title line and generator comment', () => {
    const output = exportSpiceNetlist(makeInput({ circuitName: 'Amplifier' }));
    expect(output.netlist).toContain('* Amplifier');
    expect(output.netlist).toContain('* Generated by ProtoPulse');
  });

  it('includes .END directive', () => {
    const output = exportSpiceNetlist(makeInput());
    expect(output.netlist).toContain('.END');
  });

  it('includes .CONTROL block', () => {
    const output = exportSpiceNetlist(makeInput());
    expect(output.netlist).toContain('.CONTROL');
    expect(output.netlist).toContain('run');
    expect(output.netlist).toContain('.ENDC');
  });
});

