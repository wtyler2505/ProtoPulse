describe('exportSpiceNetlist — analysis types', () => {
  it('generates .OP card for operating point analysis', () => {
    const output = exportSpiceNetlist(makeInput({
      config: makeConfig({ analysis: 'op' }),
    }));
    expect(output.netlist).toContain('.OP');
    expect(output.netlist).toContain('print all');
  });

  it('generates .TRAN card for transient analysis', () => {
    const output = exportSpiceNetlist(makeInput({
      config: makeConfig({
        analysis: 'tran',
        transient: { startTime: 0, stopTime: 0.001, timeStep: 0.000001 },
      }),
    }));
    expect(output.netlist).toContain('.TRAN');
    expect(output.netlist).toContain('plot all');
  });

  it('generates .AC card for AC analysis', () => {
    const output = exportSpiceNetlist(makeInput({
      config: makeConfig({
        analysis: 'ac',
        ac: { startFreq: 1, stopFreq: 1000000, numPoints: 100, sweepType: 'dec' },
      }),
    }));
    expect(output.netlist).toContain('.AC DEC');
  });

  it('generates .DC card for DC sweep analysis', () => {
    const output = exportSpiceNetlist(makeInput({
      config: makeConfig({
        analysis: 'dc',
        dcSweep: { sourceName: 'V1', startValue: 0, stopValue: 5, stepValue: 0.1 },
      }),
    }));
    expect(output.netlist).toContain('.DC V1');
  });

  it('includes .TEMP directive for non-default temperature', () => {
    const output = exportSpiceNetlist(makeInput({
      config: makeConfig({ temperature: 85 }),
    }));
    expect(output.netlist).toContain('.TEMP 85');
  });

  it('omits .TEMP for default temperature (27)', () => {
    const output = exportSpiceNetlist(makeInput({
      config: makeConfig({ temperature: 27 }),
    }));
    expect(output.netlist).not.toContain('.TEMP');
  });
});

