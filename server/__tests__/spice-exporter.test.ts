describe('exportSpiceNetlist — node mapping', () => {
  it('assigns node 0 to ground nets', () => {
    const output = exportSpiceNetlist(makeInput({
      nets: [
        makeNet({ id: 1, name: 'GND', netType: 'ground' }),
        makeNet({ id: 2, name: 'VCC', netType: 'power' }),
      ],
    }));

    expect(output.netlist).toContain('0 = GND');
  });

  it('assigns node 0 for common ground net names', () => {
    for (const groundName of ['GND', 'AGND', 'DGND', 'VSS', '0']) {
      const output = exportSpiceNetlist(makeInput({
        nets: [makeNet({ id: 1, name: groundName, netType: 'signal' })],
      }));
      expect(output.netlist).toContain(`0 = ${groundName}`);
    }
  });

  it('warns when no ground net is present', () => {
    const output = exportSpiceNetlist(makeInput({
      instances: [makeInstance({
        id: 1, partId: 100, referenceDesignator: 'R1',
        properties: { value: '1K' },
      })],
      nets: [makeNet({ id: 1, name: 'VCC', netType: 'power' })],
      parts: [makePart({ id: 100 })],
    }));

    expect(output.warnings.some(w => w.toLowerCase().includes('ground'))).toBe(true);
  });
});

