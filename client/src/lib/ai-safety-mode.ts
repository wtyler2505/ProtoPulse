/**
 * Manages the AI safety mode state. Singleton per application.
 * Notifies subscribers on state changes. Persists to localStorage.
 */
export class AISafetyModeManager {
  private static instance: AISafetyModeManager | null = null;

  private _enabled: boolean;
  private _dismissedActions: Set<string>;
  private subscribers: Set<() => void>;

  constructor() {
    this._enabled = true;
    this._dismissedActions = new Set();
    this.subscribers = new Set();
    this.load();
  }

  /** Get or create the singleton instance. */
  static getInstance(): AISafetyModeManager {
    if (!AISafetyModeManager.instance) {
      AISafetyModeManager.instance = new AISafetyModeManager();
    }
    return AISafetyModeManager.instance;
  }

  /** Reset singleton (for testing). */
  static resetInstance(): void {
    AISafetyModeManager.instance = null;
  }

  // -------------------------------------------------------------------------
  // State
  // -------------------------------------------------------------------------

  /** Whether safety mode is currently enabled. */
  get enabled(): boolean {
    return this._enabled;
  }

  /** Set of action types the user has permanently dismissed. */
  get dismissedActions(): ReadonlySet<string> {
    return this._dismissedActions;
  }

  // -------------------------------------------------------------------------
  // Core API
  // -------------------------------------------------------------------------

  /** Toggle safety mode on/off. */
  toggle(): void {
    this._enabled = !this._enabled;
    this.save();
    this.notify();
  }

  /** Explicitly set safety mode on or off. */
  setEnabled(enabled: boolean): void {
    if (this._enabled !== enabled) {
      this._enabled = enabled;
      this.save();
      this.notify();
    }
  }

  /** Classify an action by its type string. */
  classifyAction(actionType: string): SafetyClassification {
    if (DESTRUCTIVE_ACTION_TYPES.has(actionType)) {
      return 'destructive';
    }
    if (CAUTION_ACTION_TYPES.has(actionType)) {
      return 'caution';
    }
    return 'safe';
  }

  /** Get a beginner-friendly explanation for an action type. */
  getTeachingExplanation(actionType: string): string {
    return TEACHING_EXPLANATIONS[actionType] ?? `This action will perform: ${actionType.replace(/_/g, ' ')}.`;
  }

  /** Get the full safety info for an action type. */
  getSafetyInfo(actionType: string): SafetyInfo {
    const classification = this.classifyAction(actionType);
    const explanation = this.getTeachingExplanation(actionType);
    const consequences = ACTION_CONSEQUENCES[actionType] ?? [
      { description: `The "${actionType.replace(/_/g, ' ')}" operation will be performed` },
    ];
    return { classification, explanation, consequences };
  }

  /**
   * Check whether an action needs confirmation.
   * Returns true if safety mode is on, the action is caution/destructive,
   * and the user has not dismissed this action type.
   */
  needsConfirmation(actionType: string): boolean {
    if (!this._enabled) {
      return false;
    }
    const classification = this.classifyAction(actionType);
    if (classification === 'safe') {
      return false;
    }
    if (this._dismissedActions.has(actionType)) {
      return false;
    }
    return true;
  }

  /**
   * Filter a list of actions into those that need confirmation and those that
   * are safe to execute immediately.
   */
  filterActions(actionTypes: string[]): {
    needsConfirmation: string[];
    safe: string[];
  } {
    const needs: string[] = [];
    const safe: string[] = [];
    for (const actionType of actionTypes) {
      if (this.needsConfirmation(actionType)) {
        needs.push(actionType);
      } else {
        safe.push(actionType);
      }
    }
    return { needsConfirmation: needs, safe };
  }

  /** Permanently dismiss confirmation for a specific action type. */
  dismissAction(actionType: string): void {
    this._dismissedActions.add(actionType);
    this.saveDismissed();
    this.notify();
  }

  /** Re-enable confirmation for a previously dismissed action type. */
  undismissAction(actionType: string): void {
    this._dismissedActions.delete(actionType);
    this.saveDismissed();
    this.notify();
  }

  /** Check if a specific action type has been dismissed. */
  isDismissed(actionType: string): boolean {
    return this._dismissedActions.has(actionType);
  }

  /** Clear all dismissed actions (re-enable confirmation for everything). */
  clearDismissed(): void {
    this._dismissedActions.clear();
    this.saveDismissed();
    this.notify();
  }

  // -------------------------------------------------------------------------
  // Subscribe / notify
  // -------------------------------------------------------------------------

  /** Subscribe to state changes. Returns an unsubscribe function. */
  subscribe(fn: () => void): () => void {
    this.subscribers.add(fn);
    return () => {
      this.subscribers.delete(fn);
    };
  }

  private notify(): void {
    for (const fn of Array.from(this.subscribers)) {
      fn();
    }
  }

  // -------------------------------------------------------------------------
  // Persistence
  // -------------------------------------------------------------------------

  private load(): void {
    try {
      const raw = localStorage.getItem(STORAGE_KEY);
      if (raw !== null) {
        this._enabled = raw === 'true';
      }
      const dismissedRaw = localStorage.getItem(DISMISSED_KEY);
      if (dismissedRaw) {
        const parsed = JSON.parse(dismissedRaw) as string[];
        if (Array.isArray(parsed)) {
          this._dismissedActions = new Set(parsed);
        }
      }
    } catch {
      // Ignore — use defaults
    }
  }

  private save(): void {
    try {
      localStorage.setItem(STORAGE_KEY, String(this._enabled));
    } catch {
      // Ignore storage errors
    }
  }

  private saveDismissed(): void {
    try {
      localStorage.setItem(DISMISSED_KEY, JSON.stringify(Array.from(this._dismissedActions)));
    } catch {
      // Ignore storage errors
    }
  }
}

