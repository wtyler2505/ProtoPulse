/**
 * AI Safety Mode Manager
 *
 * Provides a beginner-friendly safety layer for AI actions. When enabled (default),
 * caution and destructive actions require explicit user confirmation with
 * beginner-friendly explanations of what the action will do.
 *
 * Usage:
 *   const manager = AISafetyModeManager.getInstance();
 *   const classification = manager.classifyAction(action);
 *   if (classification !== 'safe') {
 *     // show confirmation dialog
 *   }
 *
 * React hook:
 *   const { enabled, classification, explanation, toggle, isDismissed, dismissAction } = useAISafetyMode();
 */

import { useCallback, useEffect, useState } from 'react';

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/** Safety classification for an AI action. */
export type SafetyClassification = 'safe' | 'caution' | 'destructive';

/** Consequence entry shown in the confirmation dialog. */
export interface ActionConsequence {
  description: string;
}

/** Full safety info for an action, including classification, explanation, and consequences. */
export interface SafetyInfo {
  classification: SafetyClassification;
  explanation: string;
  consequences: ActionConsequence[];
}

