/**
 * React hook for using the AI safety mode manager.
 * Re-renders on state changes.
 */
export function useAISafetyMode(): {
  enabled: boolean;
  toggle: () => void;
  setEnabled: (v: boolean) => void;
  classifyAction: (actionType: string) => SafetyClassification;
  getTeachingExplanation: (actionType: string) => string;
  getSafetyInfo: (actionType: string) => SafetyInfo;
  needsConfirmation: (actionType: string) => boolean;
  dismissAction: (actionType: string) => void;
  undismissAction: (actionType: string) => void;
  isDismissed: (actionType: string) => boolean;
  clearDismissed: () => void;
} {
  const manager = AISafetyModeManager.getInstance();
  const [, setTick] = useState(0);

  useEffect(() => {
    return manager.subscribe(() => {
      setTick((t) => t + 1);
    });
  }, [manager]);

  const toggle = useCallback(() => {
    manager.toggle();
  }, [manager]);

  const setEnabled = useCallback((v: boolean) => {
    manager.setEnabled(v);
  }, [manager]);

  const classifyAction = useCallback((actionType: string) => {
    return manager.classifyAction(actionType);
  }, [manager]);

  const getTeachingExplanation = useCallback((actionType: string) => {
    return manager.getTeachingExplanation(actionType);
  }, [manager]);

  const getSafetyInfo = useCallback((actionType: string) => {
    return manager.getSafetyInfo(actionType);
  }, [manager]);

  const needsConfirmation = useCallback((actionType: string) => {
    return manager.needsConfirmation(actionType);
  }, [manager]);

  const dismissAction = useCallback((actionType: string) => {
    manager.dismissAction(actionType);
  }, [manager]);

  const undismissAction = useCallback((actionType: string) => {
    manager.undismissAction(actionType);
  }, [manager]);

  const isDismissed = useCallback((actionType: string) => {
    return manager.isDismissed(actionType);
  }, [manager]);

  const clearDismissed = useCallback(() => {
    manager.clearDismissed();
  }, [manager]);

  return {
    enabled,
    toggle,
    setEnabled,
    classifyAction,
    getTeachingExplanation,
    getSafetyInfo,
    needsConfirmation,
    dismissAction,
    undismissAction,
    isDismissed,
    clearDismissed,
  };
}
